      <h3 id="what-is-a-cookie">What is a cookie?</h3>

      <p>
        A cookie is a small text file that is placed on a computer or other
        device and is used to identify the user or device and to collect
        information. Cookies are typically assigned to one of four categories
        depending on their function and intended purpose: absolutely necessary
        cookies, performance cookies, functional cookies, and cookies for
        marketing purposes.
      </p>

      <h3 id="types-of-cookies-and-why-we-use-them">
        Types of cookies and why we use them
      </h3>

      <ul>
        <li>
          <strong>Absolutely necessary cookies</strong>: These cookies are
          essential to enable you to move around a website and use its features.
          Without these cookies, services you have asked for, like adding items
          to an online shopping cart, cannot be provided.
        </li>

        <li>
          <strong>Performance cookies</strong>: These cookies collect
          information about how you use our websites. Information collected
          includes, for example, the Internet browsers and operating systems
          used, the domain name of the website previously visited, the number of
          visits, average duration of visit, and pages viewed. These cookies
          don’t collect information that personally identifies you and only
          collect aggregated and anonymous information. Performance cookies are
          used to improve the user-friendliness of a website and enhance your
          experience.
        </li>

        <li>
          <strong>Functionality cookies</strong>: These cookies allow the
          website to remember choices you make (such as your username or ID,
          language preference, or the area or region you are in) and provide
          enhanced, more personal features. These cookies can also be used to
          remember changes you have made to text size, fonts, and other
          customizable parts of web pages. They may also be used to provide
          services you have asked for, such as watching a video or commenting on
          a blog. The information these cookies collect may be anonymized, and
          they cannot track your browsing activity on other websites.
        </li>

        <li>
          <strong>Targeting and advertising cookies</strong>: These cookies
          track browsing habits and are used to deliver targeted
          (interest-based) advertising. They are also used to limit the number
          of times you see an ad and to measure the effectiveness of advertising
          campaigns. They are usually placed by advertising networks with the
          website operator’s permission. They remember that you have visited a
          website and this information is shared with other organizations, such
          as advertisers.
        </li>
      </ul>

